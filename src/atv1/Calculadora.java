public class Calculadora {
    
    public int Somar(int n1, int n2){                
        return n1+n2;
    }
    
    public int Sub(int n1, int n2){                
        return n1-n2;
    }
    
    public int Mult(int n1, int n2){                
        return n1*n2;
    }
    
    public int Div(int n1, int n2){                
        return n1/n2;
    }
    
    public int Quad(int n1){                
        return n1*n1;
    }
}
